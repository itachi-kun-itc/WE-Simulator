const readMapEntries = (source, declaration) => {
  const marker = `const ${declaration} = new Map([`;
  const start = source.indexOf(marker);
  if (start < 0) return [];
  const arrayStart = start + marker.length - 1;
  const lineStart = source.lastIndexOf("\n", start) + 1;
  const indent = source.slice(lineStart, start);
  const end = source.indexOf(`\n${indent}]);`, arrayStart);
  if (end < 0) return [];
  return vm.runInNewContext(source.slice(arrayStart, end + indent.length + 2), Object.create(null));
};

