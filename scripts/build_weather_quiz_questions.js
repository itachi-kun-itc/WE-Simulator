const getShortChoiceCandidates = (value) => {
  const normalized = normalizeChoiceTerm(value);
  const clauses = normalized.split(/[、。]/u).map((part) => part.trim()).filter(Boolean);
  const candidates = [normalized, ...clauses];
  clauses.forEach((clause) => {
    const topicParts = clause.split(/(?:では|とは|には|からは|では|は)/u).map((part) => part.trim()).filter(Boolean);
    candidates.push(...topicParts);
    const predicate = clause.match(/(?:が|を|に|で|から|より)([^、。]{2,18})$/u)?.[1];
    if (predicate) candidates.push(predicate);
    const subject = clause.match(/^([^、。]{2,18}?)(?:が|を|に|で|から|より)/u)?.[1];
    if (subject) candidates.push(subject);
  });
  return [...new Set(candidates.map(normalizeChoiceTerm))]
    .filter((label) => label.length >= 1 && label.length <= 18 && !/[、。]/u.test(label));
};

const selectUniqueShortChoices = (candidateSets, index = 0, selected = []) => {
  if (index >= candidateSets.length) return selected;
  for (const candidate of candidateSets[index]) {
    if (selected.includes(candidate)) continue;
    const result = selectUniqueShortChoices(candidateSets, index + 1, [...selected, candidate]);
    if (result) return result;
  }
  return null;
};

const compactChoiceSet = (choices) => {
  const values = choices.map(normalizeChoiceTerm);
  if (values.every((label) => label.length <= 18 && !/[、。]/u.test(label))) return values;

  const prefixLength = commonAffixLength(values);
  const withoutPrefix = values.map((value) => value.slice(prefixLength).trim());
  const suffixLength = commonAffixLength(withoutPrefix, true);
  const differences = withoutPrefix.map((value) => (
    suffixLength ? value.slice(0, -suffixLength).trim() : value
  ));
  if (
    differences.every((label) => label.length >= 1 && label.length <= 18 && !/[、。]/u.test(label))
    && new Set(differences).size === differences.length
  ) {
    return differences;
  }

  const shortChoices = selectUniqueShortChoices(
    values.map((value, index) => getShortChoiceCandidates(differences[index] || value)),
  );
  if (shortChoices) return shortChoices;

  // 意味が欠ける省略記号は使わない。共通部分を除けない場合は全文を残し、
  // 個別監査で短い選択肢へ置き換える対象として検出する。
  return values;
};

