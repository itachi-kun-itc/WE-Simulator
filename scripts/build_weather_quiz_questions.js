const cleanSentence = (value) => {
  const text = String(value || "").trim();
  return text ? `${text.replace(/[。．]+$/u, "")}。` : "";
};

const firstSentence = (value) => cleanSentence(String(value || "").split("。")[0]);
const isNegative = (value) => /(?:ません|ない|ありません|できません|なりません|不要|対象外|異なります|判断できません|限りません)/u.test(value);

const makeAssertion = (question, claim) => {
  const q = String(question || "").trim().replace(/[。？?]+$/u, "");
  const c = firstSentence(claim);
  const bareClaim = c.replace(/。$/u, "");
  if (bareClaim.length >= 20 && /^[^。、]{2,45}(?:は|が|では|には|によって|の場合|とき)、?/u.test(bareClaim)) return c;

  if (!/(?:何|どれ|どちら|どのよう|どこ|誰|いつ|いくつ|何km|何％|何日|何時間|何hPa|答えて|挙げて|ですか、|ますか、)/u.test(q)
    && /(?:ですか|ますか)$/u.test(q)) {
    if (/ですか$/u.test(q)) return cleanSentence(q.replace(/ですか$/u, isNegative(c) ? "ではありません" : "です"));
    return cleanSentence(q.replace(/ますか$/u, isNegative(c) ? "ません" : "ます"));
  }

  const topicEnd = Math.max(q.lastIndexOf("は"), q.lastIndexOf("では"));
  if (topicEnd >= 2) {
    const suffixLength = q.startsWith("では", topicEnd) ? 2 : 1;
    const topic = q.slice(0, topicEnd + suffixLength);
    return cleanSentence(`${topic}${bareClaim}`);
  }

  const objectQuestion = q.match(/^(.+?)(?:を|が)(?:何|どのよう|どこ|誰|いつ|いくつ)/u);
  if (objectQuestion) return cleanSentence(`${objectQuestion[1]}は${bareClaim}`);
  return c;
};

const mutateNumber = (text, step) => {
  let changed = false;
  const result = text.replace(/(?<!第)(\d+(?:\.\d+)?)/u, (match) => {
    if (changed) return match;
    changed = true;
    const value = Number(match);
    const factors = [0.5, 1.5, 2];
    const next = value === 0 ? step + 1 : value * factors[step % factors.length];
    return Number.isInteger(value) ? String(Math.max(1, Math.round(next))) : String(Number(next.toFixed(2)));
  });
  return changed ? result : "";
};

const mutateTerms = (text) => {
  const variants = [];
  for (const group of TERM_GROUPS) {
    const found = group.find((term) => text.includes(term));
    if (!found) continue;
    for (const replacement of group) {
      if (replacement !== found) variants.push(text.replace(found, replacement));
    }
  }
  return variants;
};

const mutatePolarity = (text) => {
  const pairs = [
    ["できません", "できます"], ["できます", "できません"],
    ["なりません", "なります"], ["なります", "なりません"],
    ["ありません", "あります"], ["あります", "ありません"],
    ["必要です", "必要ありません"], ["必要ありません", "必要です"],
    ["含まれます", "含まれません"], ["求めます", "求めません"],
    ["保存されます", "保存されません"], ["発表されます", "発表されません"],
    ["高くなります", "低くなります"], ["低くなります", "高くなります"],
    ["増加します", "減少します"], ["減少します", "増加します"],
  ];
  const pair = pairs.find(([from]) => text.includes(from));
  return pair ? text.replace(pair[0], pair[1]) : "";
};

